'''Faça um Programa que verifique se uma letra digitada é vogal ou consoante.
'''

letra = input('digite uma letra: ').lower()

if letra == 'a':
    print('é vogal')
else:
    if letra == 'e':
        print('é vogal')
    else:
        if letra == 'i':
            print('é vogal')
        else:
            if letra == 'o':
                print('é vogal')
            else:
                if letra == 'u':
                    print('é vogal')
                else:
                    print('é consoante')