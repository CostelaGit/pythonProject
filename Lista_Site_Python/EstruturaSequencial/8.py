"Faça um Programa que pergunte quanto você ganha por hora e o "
"número de horas trabalhadas no mês. Calcule e mostre "
"o total do seu salário no referido mês."


ghora = float(input('digite o quanto você ganha por hora:'))
nhora = float(input('digite o quanto de horas você trabalhou:'))
salariom = ghora * nhora
print('o salario do mês é:', salariom)